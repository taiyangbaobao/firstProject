public class PlusTest {
    public static void main(String[] args) {
        int a = 1;
        int b = a;
        b = ++a;
        System.out.println(b);
        b = ++a;
        System.out.println(b);
        System.out.println(++a);
        System.out.println(++a);
        System.out.println(++a);
    }
}
